import React, { useContext, useState, useEffect } from 'react';
import Fade from 'react-reveal/Fade';
import { Container, Row, Col } from 'react-bootstrap';
import Title from '../Title/Title';
import AboutImg from '../Image/AboutImg';
import PortfolioContext from '../../context/context';

const About = () => {
  const { about } = useContext(PortfolioContext);
  const { img, paragraphOne, paragraphTwo, paragraphThree, resume } = about;

  const [isDesktop, setIsDesktop] = useState(false);
  const [isMobile, setIsMobile] = useState(false);

  useEffect(() => {
    if (window.innerWidth > 769) {
      setIsDesktop(true);
      setIsMobile(false);
    } else {
      setIsMobile(true);
      setIsDesktop(false);
    }
  }, []);

  return (
    <section id="about">
      <Container>
        <Title title="About Me" />
        <Row className="about-wrapper">
          <Col md={6} sm={12}>
            <Fade bottom duration={1000} delay={600} distance="30px">
              <div className="about-wrapper__image">
                <AboutImg alt="profile picture" filename={img} />
              </div>
            </Fade>
          </Col>
          <Col md={6} sm={12}>
            <Fade left={isDesktop} bottom={isMobile} duration={1000} delay={1000} distance="30px">
              <div className="about-wrapper__info">
                <p className="about-wrapper__info-text">
                  {paragraphOne ||
                    'I am a Web Developer from Dallas ,Texas let me introduce myself I have started my career in coding as a Web Designer I am passionate about applying responsive design principles and watching my web pages shrinks into mobile screen and still look amazing'}
                </p>
                <p className="about-wrapper__info-text">
                  {paragraphTwo ||
                    ' My speciality include quick learning new skill and programming language-  Technical skill 路 Frontend: JavaScript, jQuery, CSS, Bootstrap, HTML, AJAX/Axios 路 Backend: Python/Flask and Node/Express 路 Testing: Jasmine, Jest, Python unittest (unit tests and integration tests)路 Database and related: PostgreSQL, SQLAlchemy. React Js'}
                </p>
                <p className="about-wrapper__info-text">
                  {paragraphThree || 'Independent Work,Group work,Team Motivation,Critical Thinking,Deescalating High Pressure/Emotional Situations.'}
                </p>
                {resume && (
                  <span className="d-flex mt-3">
                    <a
                      target="_blank"
                      rel="noopener noreferrer"
                      className="cta-btn cta-btn--resume"
                      href={resume}
                    >
                      Resume
                    </a>
                  </span>
                )}
              </div>
            </Fade>
          </Col>
        </Row>
      </Container>
    </section>
  );
};

export default About;
